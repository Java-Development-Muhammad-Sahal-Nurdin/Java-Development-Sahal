
public class MethodParameter 
{
    public static void main(String[] args) 
    {
        sayHello("Muhammad Sahal", "Nurdin");
        sayHello("Tony", "Stark");
    }
    static void sayHello(String firstName, String lastName) // ADA PARAMETER
    {
        System.out.println("Hello " + firstName +" " +  lastName);
    }
}
