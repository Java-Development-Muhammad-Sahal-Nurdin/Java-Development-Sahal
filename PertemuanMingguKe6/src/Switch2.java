public class Switch2 {
    public static void main (String[] args)
    {
        int a = 3;
        switch(a){
            case 1:
                    System.out.println("Nilai 1"); break;
            case 2:
                    System.out.println("Nilai 2"); break;
            case 3:
                    System.out.println("Nilai 3"); break;
            default:
                    System.out.println("Nilai tidak match");
        }
    }
    
}
